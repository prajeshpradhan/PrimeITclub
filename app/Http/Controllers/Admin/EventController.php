    {
        $events = Event::latest()->get();
        foreach ($events as $event) {
            $event->date = Carbon::parse($event->date)->toFormattedDateString();
            $event->time = Carbon::parse($event->time)->format('h:i A');
        }
        return view('admin.event.index')->with('events', $events);
    }

